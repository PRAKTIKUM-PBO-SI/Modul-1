public class Mahasiswaa {
     String nama;
     float nilai1;
     float nilai2;
     float nilai3;

    public Mahasiswaa(String nama, float nilai1, float nilai2, float nilai3) {
        this.nama = nama;
        this.nilai1 = nilai1;
        this.nilai2 = nilai2;
        this.nilai3 = nilai3;
    }

    public String nama() {
        return nama;
    }

    public float nilai1() {
        return nilai1;
    }

    public float nilai2() {
        return nilai2;
    }

    public float nilai3() {
        return nilai3;
 }
}
